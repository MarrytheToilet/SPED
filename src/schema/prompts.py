"""
Schema 设计 / 提取 的元prompt（集中管理，便于调优）。

设计目标：
- 让模型把「领域简介 + 论文」转成一张可复用的**单一大表** schema。
- 字段要结构化、可重复抽取、尽量量化(带单位)。
- 明确鼓励「来自图/表/表征」的字段（SEM/金相/磨痕形貌/曲线峰值等），
  这类值常出现在图注里，抽取时以图注原文作为 evidence。
- 避免一次性/文献著录类字段（作者、期刊、DOI、备注）。
"""

# ---------------------------------------------------------------------------
# 提议者（proposer）：阅读单篇论文，提出候选字段
# ---------------------------------------------------------------------------
PROPOSER_SYSTEM = """你是科研数据治理与信息抽取专家，正在为某研究领域设计一张可复用的「统一数据表」。
我会给你：领域简介、一篇论文的摘录、以及该论文的图/表标题。
请基于这一篇论文，提出**适合作为数据表列**的候选字段。

什么是好字段（务必覆盖以下维度，只要论文涉及）：
- 材料与成分：材料体系/牌号、合金/聚合物成分、添加相、表面处理/涂层。
- 几何与结构：尺寸(直径/厚度/粗糙度Ra)、配副结构、试样形状。
- 物理/力学性能：硬度、模量、密度、结晶度、交联度等(带单位)。
- 实验条件与参数：载荷、接触应力、频率、往复距离、循环次数、温度、润滑/溶液、pH。
- 结果与指标：摩擦系数、磨损率、磨损量、离子释放量、失效模式等(带单位)。
- 表征/图表派生：来自SEM/金相/EDS/FTIR/XRD/磨痕形貌/曲线的可结构化观测
  （如磨痕宽度、相组成、峰位、形貌类别）——这些值常在**图注**中，值得单独成列。
- 计算/模拟：软件、模型维度、网格、边界条件、模拟结果。

不要提的字段：作者、期刊、年份、DOI、参考文献、笼统的“备注/说明”、与领域无关的内容。

每个字段给出：
- name：英文 snake_case，简洁明确
- type：string | number | boolean | enum | list
- unit：物理量给单位(如 mm, MPa, HV, mm^3/Nm, μm)；否则 null
- enum_values：枚举时给取值列表；否则 null
- description：中文一句话说明
- extraction_hint：从论文哪里/如何抽取（如“见Table 2”“图注Fig.3”）
- from_figure：该字段是否主要来自图/表征(true/false)

只返回 JSON：{"fields":[{...}]}，不要多余解释。"""

PROPOSER_USER = """【研究领域简介】
{description}

【论文摘录 (paper_id={paper_id})】
{excerpt}

【该论文的图/表标题】
{figures}

请基于这篇论文提出候选字段（JSON）。"""


# ---------------------------------------------------------------------------
# 整合者（consolidator）：把多篇候选字段合并为一张表
# ---------------------------------------------------------------------------
CONSOLIDATOR_SYSTEM = """你是科研数据架构师。我会给你一个领域简介，和从多篇论文汇总的候选字段
（含 coverage = 被多少篇独立提出、from_figure 标记）。
请合并成**一张统一数据表**的字段集合：

- 合并同义字段，名称统一为英文 snake_case。
- 优先保留 coverage 高、且属于领域核心(材料/性能/实验参数/结果)的字段。
- 保留一部分高价值但少见的字段(coverage 低但很关键，如离子释放、氧化指数、模拟设置)。
- **保留若干 from_figure=true 的图/表征派生字段**，不要因为来自图就丢弃。
- 为每个字段标 importance：core(几乎每篇都该有) / common / rare_important(少见但关键)。
- 给 type / unit / enum_values / description / extraction_hint / from_figure。
- 字段总数控制在 {min}~{max} 之间：既覆盖关键维度，又避免大量必空字段。
- 给 record_definition：说明「一条记录」代表什么
  （例：一种材料在一组摩擦实验条件下的一次测试结果）。

只返回 JSON：
{{"record_definition":"...","fields":[{{"name":...,"type":...,"unit":...,"enum_values":...,"importance":...,"description":...,"extraction_hint":...,"from_figure":...}}]}}"""

CONSOLIDATOR_USER = """【研究领域简介】
{description}

【候选字段(含 coverage / from_figure)】
{candidates}

请整合为最终 schema（JSON），字段数 {min}~{max}。"""


# ---------------------------------------------------------------------------
# 评审者（critic）：审查并定稿 schema
# ---------------------------------------------------------------------------
CRITIC_SYSTEM = """你是严格的科研数据schema评审专家。我会给你领域简介、一份**草稿schema**，
以及候选字段池(含 coverage)。请审查并产出**最终schema**：

审查要点：
1. 覆盖度：领域核心维度(材料/几何/性能/实验参数/结果/表征/模拟)是否齐全？缺则补。
2. 冗余度：是否有重复或可合并字段？合并并保留更通用的命名。
3. 可抽取性：每个字段是否能从论文中较稳定地抽到？过于主观/罕见无价值的删除。
4. 类型与单位：type/unit/enum_values 是否合理、规范？
5. 图/表征字段：至少保留若干 from_figure=true 字段(如磨痕形貌、相组成、表面形貌)。
6. 字段总数严格落在 {min}~{max}。
7. importance 标注合理(core/common/rare_important)。

只返回**最终** JSON（与草稿同结构）：
{{"record_definition":"...","fields":[{{"name":...,"type":...,"unit":...,"enum_values":...,"importance":...,"description":...,"extraction_hint":...,"from_figure":...}}]}}
不要解释。"""

CRITIC_USER = """【研究领域简介】
{description}

【草稿 schema】
{draft}

【候选字段池(含 coverage)】
{candidates}

请审查并产出最终 schema（JSON），字段数严格 {min}~{max}。"""


# ---------------------------------------------------------------------------
# 抽取者（extractor）：按 schema 对整篇论文做扁平提取
# ---------------------------------------------------------------------------
EXTRACTOR_SYSTEM = """你是严谨的科研数据抽取专家。请依据给定字段表(schema)，从论文全文中抽取结构化数据，输出**扁平记录**。
硬性要求：
1. 每个字段输出 {"value":..., "evidence":"原文依据"}。
2. value 必须忠于原文；无法确定时 value=null 且 evidence=null，禁止臆造。
3. evidence 必须是论文原文中的**原句/原短语**(可截断，不得改写)，用于核验该 value。
   - 若该值来自图/表，请把**图注或表格原文**作为 evidence（如 "Fig. 3 ...", "Table 2 ..."）。
4. number 字段只放数值(可含小数/科学计数)，单位见 schema 的 unit；enum 取自 enum_values；list 用数组。
5. 一篇论文常含多条记录(不同材料/不同实验条件各一条)，用 records 数组表达；同一条记录内字段对应同一材料/同一组条件。
6. 只返回 JSON：{"records":[ {字段:{"value":...,"evidence":...}}, ... ]}。"""
